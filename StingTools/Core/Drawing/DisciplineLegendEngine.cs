    /// <summary>
    /// W1/W2 — shared discipline symbol-legend builder.
    ///
    /// Factored out of <c>ElectricalDrawingLegendCommand</c> so the exact same
    /// engine drives (a) the standalone Mechanical / Plumbing / Structural
    /// legend commands and (b) the <see cref="DrawingProducer"/> Legend
    /// production path (a Legend-purpose DrawingType now mints a populated
    /// legend instead of falling through to a FloorPlan).
    ///
    /// Strategy mirrors the electrical legend: walk every family instance in a
    /// discipline's categories, dedupe by (category, family, type), resolve
    /// each to its STING symbol concept via <see cref="SymbolConceptRegistry"/>,
    /// and render one row per unique symbol through the Phase 92
    /// <see cref="LegendBuilder"/> engine. When a discipline has no placed
    /// components (or the discipline is General "G"), it falls back to a
    /// discipline-colour legend so the produced view is never empty.
    ///
    /// It does NOT read the <c>STING_*_SYMBOLS.json</c> catalogues directly —
    /// those describe symbols to *create*; this view documents which symbols a
    /// project actually uses and to which standard, exactly like the electrical
    /// command it generalises.
    /// </summary>
    public static class DisciplineLegendEngine
    {
